
package javaapplication65;


public class Nombres {
    
    private String nombre01;
    private String nombre02;

    public Nombres(String nombre01, String nombre02) {
        this.nombre01 = nombre01;
        this.nombre02 = nombre02;
    }

    public String getNombre01() {
        return nombre01;
    }

    public void setNombre01(String nombre01) {
        this.nombre01 = nombre01;
    }

    public String getNombre02() {
        return nombre02;
    }

    public void setNombre02(String nombre02) {
        this.nombre02 = nombre02;
    }
    
    
    
}
